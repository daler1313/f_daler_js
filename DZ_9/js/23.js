function fa(day){
    if (day == 1) {
            console.log('Понедельник');
        } else if (day == 2) {
            console.log('Вторнк');
        } else if (day == 3) {
            console.log('Среда');
        } else if (day == 4) {
            console.log('Четверг');
        } else if (day == 5) {
            console.log('Пятница');
        } else if (day == 6) {
            console.log('Суббота');
        } else if (day == 7) {
            console.log('Воскресенье');
        }
        else{
            console.log('Нужна было вести цифры от 1 до 7'); 
        }
}
fa(2);